package exam;

public class Fruit {
	String fruit = "";
	String color = "";

	public Fruit(String fruit, String color) {
		this.fruit = fruit;
		this.color = color;
	}

	public String getFruit() {
		return fruit;
	}

	public void setFruit(String fruit) {
		this.fruit = fruit;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}
	

}
